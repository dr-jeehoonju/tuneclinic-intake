export function buildSystemPrompt(
  state: string,
  fieldsCollected: string[],
  fieldsMissing: string[],
  quickSummary?: string
): string {
  let prompt = BASE_PROMPT;

  const injection = STATE_INJECTIONS[state];
  if (injection) {
    let injected = injection;
    if (state === "structured_gathering") {
      injected = injected
        .replace(
          "{fields_collected}",
          fieldsCollected.length > 0
            ? fieldsCollected.map((f) => `- ${f}`).join("\n")
            : "- (아직 없음)"
        )
        .replace(
          "{fields_missing}",
          fieldsMissing.length > 0
            ? fieldsMissing.map((f) => `- ${f}`).join("\n")
            : "- (모두 수집 완료)"
        );
    }
    if (state === "deep_gather") {
      injected = injected
        .replace(
          "{quick_summary}",
          quickSummary || "(사전 설문 데이터 없음)"
        )
        .replace(
          "{fields_missing}",
          fieldsMissing.length > 0
            ? fieldsMissing.map((f) => `- ${f}`).join("\n")
            : "- (추가 수집 불필요)"
        );
    }
    prompt += "\n\n" + injected;
  }

  return prompt;
}

export function computeMissingFields(collected: string[]): string[] {
  return ALL_FIELDS.filter((f) => !collected.includes(f));
}
